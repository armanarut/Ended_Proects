int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				m;

	m = 0;
	i = 0;
	while (i < n && (s1[i] || s2[i]))
	{	
		if (s1[i] != s2[i])
		{
			m = s1[i] - s2[i];
			break ;
		}
		i++;
	}
	return (m);
}
